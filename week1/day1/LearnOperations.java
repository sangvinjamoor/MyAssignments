package week1.day1;

public class LearnOperations {

	public static void main(String[] args) {
		int a = 20;
		int b=10;
		int c = 2;
		int d = 10;
		System.out.println(c/d);
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
		System.out.println(a + "  " + a++ + "  " +a);
		System.out.println(a + "  "  + ++a + "  " +a);
		
		for (int i =0; i <=5; i++) {
			System.out.println(i+1);
		}
		
	}

}
